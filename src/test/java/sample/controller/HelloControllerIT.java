/**
 * 実際に組み込みサーバを立ち上げて行うコントローラのテスト
 * @SpringApplicationConfigurationにApplication（実際のアプリケーション起動クラス）を指定する。
 * @IntegrationTest({"server.port=0"})でランダムなポートサーバを起動する。
 */
@RunWith(SpringJUnit4ClassRunner.class)
@SpringApplicationConfiguration(classes = Application.class)
@WebAppConfiguration
@IntegrationTest({"server.port=0"})
public class HelloControllerIT {

    @Value("${local.server.port}") // 実際のポート番号が実行時にインジェクトされる。
    private int port;

    private URL base;
    private RestTemplate template;

    @Before
    public void setUp() throws Exception {
        this.base = new URL("http://localhost:" + port + "/");
        template = new TestRestTemplate();
    }

    @Test
    public void getHello() throws Exception {
        ResponseEntity<String> response = template.getForEntity(base.toString() + "hello", String.class);
        assertThat(response.getBody(), equalTo("Hello Get"));
    }
}
